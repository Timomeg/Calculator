#include "calc.h"
// синус
double sinus(double x) {
    return sin(x);
}
// косинус
double cosine(double x) {
    return cos(x);
}
// тангенс
double tangent(double x) {
    return tan(x);
}
// котангенс
double cotangent(double x) {
    return 1.0/tan(x);
}
// экспонента
double exponent(double x) {
    return exp(x);
}
